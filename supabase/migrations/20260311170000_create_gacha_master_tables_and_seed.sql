-- Create gacha master tables and seed existing gacha definitions.

begin;

create table if not exists master.m_gacha (
  gacha_id bigint generated by default as identity primary key,
  gacha_code text not null unique,
  gacha_name text not null,
  description text,
  rarity_rate_n numeric(5,4) not null default 0,
  rarity_rate_r numeric(5,4) not null default 0,
  rarity_rate_sr numeric(5,4) not null default 0,
  rarity_rate_ur numeric(5,4) not null default 0,
  rarity_rate_ssr numeric(5,4) not null default 0,
  image_source text not null default 'supabase',
  image_bucket text,
  image_key text,
  image_version integer not null default 1,
  is_active boolean not null default true,
  published_at timestamptz,
  unpublished_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint m_gacha_publish_window_chk
    check (unpublished_at is null or published_at is null or unpublished_at > published_at),
  constraint m_gacha_image_source_chk
    check (image_source in ('supabase', 's3')),
  constraint m_gacha_image_version_chk
    check (image_version >= 1),
  constraint m_gacha_image_key_format_chk
    check (image_key is null or btrim(image_key) <> ''),
  constraint m_gacha_rate_n_chk check (rarity_rate_n >= 0 and rarity_rate_n <= 1),
  constraint m_gacha_rate_r_chk check (rarity_rate_r >= 0 and rarity_rate_r <= 1),
  constraint m_gacha_rate_sr_chk check (rarity_rate_sr >= 0 and rarity_rate_sr <= 1),
  constraint m_gacha_rate_ur_chk check (rarity_rate_ur >= 0 and rarity_rate_ur <= 1),
  constraint m_gacha_rate_ssr_chk check (rarity_rate_ssr >= 0 and rarity_rate_ssr <= 1),
  constraint m_gacha_rate_total_chk
    check (
      abs((rarity_rate_n + rarity_rate_r + rarity_rate_sr + rarity_rate_ur + rarity_rate_ssr) - 1.0) <= 0.0001
    )
);

create index if not exists m_gacha_active_publish_idx
  on master.m_gacha(is_active, published_at, unpublished_at);

create index if not exists m_gacha_image_lookup_idx
  on master.m_gacha(image_source, image_bucket, image_key);

create table if not exists master.m_gacha_piece (
  gacha_id bigint not null references master.m_gacha(gacha_id) on delete cascade,
  piece_id bigint not null references master.m_piece(piece_id),
  weight integer not null default 1,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (gacha_id, piece_id),
  constraint m_gacha_piece_weight_chk check (weight >= 1)
);

create index if not exists m_gacha_piece_piece_idx
  on master.m_gacha_piece(piece_id);

